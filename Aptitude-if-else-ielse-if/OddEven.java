//to print Odd and even numbers without using array..
class OddEven{
	public static void main(String args[]){
	System.out.println("Even numbers are");
	for(int i=1;i<=10;i++){
		if(i%2==0){
			System.out.println(i);
		}
		
	}
	System.out.println("Odd numbers are");
	for(int j=1;j<=10;j++){
			if(j%2!=0){
				System.out.println(j);
			}
		}
	} 

}